/** Build a full TSV string with trailing tabs (matching real 読専 output). */
export function buildFullTsv(): string {
  return [HEADER_ROW + '\t', ...DATA_ROWS.map((r) => r + '\t')].join('\n');
}

/** Build a full TSV string without trailing tabs. */
export function buildFullTsvNoTrailingTab(): string {
  return [HEADER_ROW, ...DATA_ROWS].join('\n');
}
