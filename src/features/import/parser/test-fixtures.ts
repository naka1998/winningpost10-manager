/** Build a TSV row from sparse column data. Unset columns default to empty string. */
function buildRow(data: Record<number, string>): string {
  const cells: string[] = Array(TOTAL_COLS).fill('');
  for (const [idx, val] of Object.entries(data)) {
    cells[Number(idx)] = val;
  }
  return cells.join('\t');
}

